// Get column data for a character
function getCharColumns(ch) {
  return FONT[ch.toUpperCase()] || FONT['?'];
}

// Convert a full string to column array with spacing
function textToColumns(text) {
  const cols = [];
  for (const ch of text) {
    const charCols = getCharColumns(ch);
    cols.push(...charCols, 0); // 1-pixel gap between chars
  }
  return cols;
}
